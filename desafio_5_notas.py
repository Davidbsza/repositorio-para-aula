# Programa: soma de duas notas

# Captura das notas
nota1 = float(input("Digite a primeira nota: "))
nota2 = float(input("Digite a segunda nota: "))

# Soma das notas
soma = nota1 + nota2

# Exibição do resultado
print("\n--- Resultado ---")
print(f"Nota 1: {nota1}")
print(f"Nota 2: {nota2}")
print(f"Soma das notas: {soma:.2f}")
